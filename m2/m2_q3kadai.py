# リストの定義
a = [2.02, 2.03, 2.04]
b = [2.05, 2.06, -2.07]

# リストを結合
c = a + b

# 最大値を求める
print(max(c))  # 2.06

# c[2:5] の最小値を求める
print(min(c[2:5]))  # 2.04

# c[2] を少数第1位に丸めて出力
print(round(c[2], 1))  # 2.0

# c[5] を少数第1位に丸めて出力
print(round(c[5], 1))  # -2.1

# c[3] を少数第1位に丸めて出力
print(round(c[3], 1))  # 2.0

# コメント：なぜ 2.0 になるのか
# 2.05 は2.0と2.1のちょうど中間の値であるため、
# Pythonのround関数は「偶数丸め（銀行丸め）」という規則に従い、
# 末尾の数が偶数になる方向に丸められる。
# よって round(2.05, 1) は 2.0 になる。
